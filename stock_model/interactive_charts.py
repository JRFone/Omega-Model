    def interval_coverage(
        self,
        rows: Sequence[Mapping[str, Any]],
        *,
        nominal_key: str = "nominal",
        empirical_key: str = "empirical",
        parameter_key: str = "parameter",
        title: str = "Interval-coverage testing",
    ):
        go, _make_subplots, _pio = _plotly()
        groups: dict[str, list[tuple[float, float]]] = {}
        for row in rows:
            nominal = _as_float(row.get(nominal_key)); empirical = _as_float(row.get(empirical_key))
            if nominal is None or empirical is None:
                continue
            groups.setdefault(str(row.get(parameter_key, "Overall")), []).append((nominal, empirical))
        figure = go.Figure()
        for index, (name, values) in enumerate(sorted(groups.items())):
            values.sort()
            figure.add_trace(
                go.Scatter(
                    x=[value[0] for value in values],
                    y=[value[1] for value in values],
                    mode="lines+markers",
                    name=name,
                    line={"width": self.profile.line_width, "color": self.profile.palette[index % len(self.profile.palette)]},
                    marker={"size": self.profile.marker_size},
                )
            )
        figure.add_trace(
            go.Scatter(x=[0, 1], y=[0, 1], mode="lines", name="Ideal coverage", line={"dash": "dash", "width": 2})
        )
        layout = self._base_layout(title, "Nominal interval coverage", "Empirical coverage")
        layout["xaxis"].update({"rangeslider": {"visible": False}, "range": [0, 1]})
        layout["yaxis"].update({"range": [0, 1]})
        figure.update_layout(**layout)
        return figure

