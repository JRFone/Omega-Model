@dataclass(frozen=True)
class ChartProfile:
    """Personal display preferences applied consistently across Omega charts."""

    name: str = "Omega default"
    template: str = "plotly_white"
    font_family: str = "Segoe UI, Arial, sans-serif"
    font_size: int = 13
    title_size: int = 20
    line_width: float = 2.5
    marker_size: float = 7.0
    show_grid: bool = True
    show_legend: bool = True
    range_slider: bool = True
    editable: bool = True
    scroll_zoom: bool = True
    show_spikes: bool = True
    hovermode: str = "x unified"
    legend_orientation: str = "h"
    background: str = "#ffffff"
    plot_background: str = "#ffffff"
    palette: tuple[str, ...] = field(default_factory=lambda: _DEFAULT_PALETTE)
    downsample_limit: int = 10_000
    default_width: int | None = None
    default_height: int = 720
    export_scale: int = 2

    def validated(self) -> "ChartProfile":
        palette = tuple(str(value) for value in self.palette if str(value).strip()) or _DEFAULT_PALETTE
        template = self.template if self.template in {
            "plotly_white", "plotly_dark", "simple_white", "ggplot2", "seaborn", "presentation", "none"
        } else "plotly_white"
        hovermode = self.hovermode if self.hovermode in {
            "x", "y", "closest", "x unified", "y unified", False
        } else "x unified"
        orientation = "v" if str(self.legend_orientation).lower().startswith("v") else "h"
        return replace(
            self,
            template=template,
            font_size=max(8, min(int(self.font_size), 32)),
            title_size=max(10, min(int(self.title_size), 48)),
            line_width=max(0.5, min(float(self.line_width), 12.0)),
            marker_size=max(1.0, min(float(self.marker_size), 30.0)),
            palette=palette,
            downsample_limit=max(200, int(self.downsample_limit)),
            default_height=max(320, int(self.default_height)),
            export_scale=max(1, min(int(self.export_scale), 8)),
            hovermode=hovermode,
            legend_orientation=orientation,
        )

    def to_dict(self) -> dict[str, Any]:
        payload = asdict(self.validated())
        payload["palette"] = list(payload["palette"])
        return payload

    @classmethod
    def from_dict(cls, payload: Mapping[str, Any]) -> "ChartProfile":
        known = {field.name for field in cls.__dataclass_fields__.values()}
        values = {key: value for key, value in dict(payload).items() if key in known}
        if "palette" in values:
            values["palette"] = tuple(values["palette"] or ())
        return cls(**values).validated()


class ChartProfileStore:
    """JSON profile store with named profiles and a selected default."""

    def __init__(self, path: str | Path) -> None:
        self.path = Path(path)

    def load_all(self) -> dict[str, ChartProfile]:
        if not self.path.exists():
            return {"Omega default": ChartProfile()}
        try:
            raw = json.loads(self.path.read_text(encoding="utf-8"))
        except (OSError, json.JSONDecodeError):
            return {"Omega default": ChartProfile()}
        profiles = raw.get("profiles", raw) if isinstance(raw, dict) else {}
        result: dict[str, ChartProfile] = {}
        if isinstance(profiles, dict):
            for name, payload in profiles.items():
                if isinstance(payload, Mapping):
                    profile = ChartProfile.from_dict({**dict(payload), "name": str(name)})
                    result[profile.name] = profile
        return result or {"Omega default": ChartProfile()}

    def save(self, profile: ChartProfile) -> None:
        profiles = self.load_all()
        profile = profile.validated()
        profiles[profile.name] = profile
        self.path.parent.mkdir(parents=True, exist_ok=True)
        payload = {
            "schema_version": 1,
            "profiles": {name: value.to_dict() for name, value in sorted(profiles.items())},
        }
        self.path.write_text(json.dumps(payload, indent=2), encoding="utf-8")

    def delete(self, name: str) -> bool:
        profiles = self.load_all()
        if name == "Omega default" or name not in profiles:
            return False
        del profiles[name]
        self.path.parent.mkdir(parents=True, exist_ok=True)
        self.path.write_text(
            json.dumps({"schema_version": 1, "profiles": {k: v.to_dict() for k, v in profiles.items()}}, indent=2),
            encoding="utf-8",
        )
        return True


