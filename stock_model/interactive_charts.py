def _plotly():
    try:
        import plotly.graph_objects as go
        from plotly.subplots import make_subplots
        import plotly.io as pio
    except ImportError as exc:  # pragma: no cover - depends on runtime packaging
        raise RuntimeError(
            "Interactive charts require Plotly. Run SETUP_OMEGA_FISH.bat or install plotly>=5.18."
        ) from exc
    return go, make_subplots, pio


def _numeric_x(values: Sequence[Any]) -> np.ndarray:
    if len(values) == 0:
        return np.asarray([], dtype=float)
    first = values[0]
    if isinstance(first, (datetime, date, np.datetime64)):
        return np.asarray([np.datetime64(value).astype("datetime64[ns]").astype(np.int64) for value in values], dtype=float)
    result = np.empty(len(values), dtype=float)
    for index, value in enumerate(values):
        try:
            result[index] = float(value)
        except (TypeError, ValueError):
            result[index] = float(index)
    return result


def lttb_downsample(
    x: Sequence[Any],
    y: Sequence[float],
    threshold: int,
) -> tuple[list[Any], list[float], list[int]]:
    """Largest-Triangle-Three-Buckets downsampling preserving peaks and endpoints.

    Returns sampled x, sampled y, and source indexes. Non-finite y observations are
    retained when the series is already below the threshold; for long series they
    are removed before the geometric sampling step.
    """

    if len(x) != len(y):
        raise ValueError("x and y must have equal length.")
    length = len(x)
    threshold = int(threshold)
    if threshold <= 0 or threshold >= length or length <= 2:
        return list(x), [float(v) for v in y], list(range(length))
    if threshold < 3:
        indexes = [0, length - 1][:threshold]
        return [x[i] for i in indexes], [float(y[i]) for i in indexes], indexes

    x_numeric = _numeric_x(x)
    y_numeric = np.asarray(y, dtype=float)
    finite = np.isfinite(x_numeric) & np.isfinite(y_numeric)
    finite_indexes = np.flatnonzero(finite)
    if len(finite_indexes) <= threshold:
        indexes = finite_indexes.tolist()
        return [x[i] for i in indexes], [float(y_numeric[i]) for i in indexes], indexes

    x_work = x_numeric[finite]
    y_work = y_numeric[finite]
    source = finite_indexes
    every = (len(x_work) - 2) / (threshold - 2)
    selected = [0]
    a = 0

    for bucket in range(threshold - 2):
        avg_start = int(np.floor((bucket + 1) * every)) + 1
        avg_end = int(np.floor((bucket + 2) * every)) + 1
        avg_end = min(avg_end, len(x_work))
        if avg_start >= avg_end:
            avg_x = x_work[min(avg_start, len(x_work) - 1)]
            avg_y = y_work[min(avg_start, len(y_work) - 1)]
        else:
            avg_x = float(np.mean(x_work[avg_start:avg_end]))
            avg_y = float(np.mean(y_work[avg_start:avg_end]))

        range_start = int(np.floor(bucket * every)) + 1
        range_end = int(np.floor((bucket + 1) * every)) + 1
        range_end = min(range_end, len(x_work) - 1)
        if range_end <= range_start:
            range_end = min(range_start + 1, len(x_work) - 1)

        point_ax = x_work[a]
        point_ay = y_work[a]
        segment_x = x_work[range_start:range_end]
        segment_y = y_work[range_start:range_end]
        areas = np.abs((point_ax - avg_x) * (segment_y - point_ay) - (point_ax - segment_x) * (avg_y - point_ay))
        offset = int(np.argmax(areas)) if len(areas) else 0
        a = min(range_start + offset, len(x_work) - 2)
        selected.append(a)

    selected.append(len(x_work) - 1)
    indexes = source[np.asarray(selected, dtype=int)].tolist()
    return [x[i] for i in indexes], [float(y_numeric[i]) for i in indexes], indexes


def _finite_range(values: Iterable[float]) -> tuple[float, float] | None:
    array = np.asarray(list(values), dtype=float)
    array = array[np.isfinite(array)]
    if not len(array):
        return None
    return float(np.min(array)), float(np.max(array))


class InteractiveChartFactory:
    """Build consistent Plotly figures for all expert-workflow diagnostics."""

    def __init__(self, profile: ChartProfile | None = None) -> None:
        self.profile = (profile or ChartProfile()).validated()

    @property
    def config(self) -> dict[str, Any]:
        return {
            "responsive": True,
            "scrollZoom": self.profile.scroll_zoom,
            "editable": self.profile.editable,
            "displaylogo": False,
            "toImageButtonOptions": {
                "format": "png",
                "filename": "omega_fish_chart",
                "height": self.profile.default_height,
                "width": self.profile.default_width or 1400,
                "scale": self.profile.export_scale,
            },
            "modeBarButtonsToAdd": [
                "drawline",
                "drawopenpath",
                "drawrect",
                "drawcircle",
                "eraseshape",
                "toggleSpikelines",
            ],
        }

    def _base_layout(self, title: str, x_title: str = "", y_title: str = "") -> dict[str, Any]:
        profile = self.profile
        legend = {
            "orientation": profile.legend_orientation,
            "yanchor": "bottom" if profile.legend_orientation == "h" else "top",
            "y": 1.02 if profile.legend_orientation == "h" else 1.0,
            "xanchor": "right" if profile.legend_orientation == "h" else "left",
            "x": 1.0 if profile.legend_orientation == "h" else 1.02,
        }
        axis_common = {
            "showgrid": profile.show_grid,
            "showspikes": profile.show_spikes,
            "spikemode": "across",
            "spikesnap": "cursor",
            "showline": True,
            "mirror": True,
            "zeroline": False,
        }
        return {
            "title": {"text": title, "font": {"size": profile.title_size}, "x": 0.01, "xanchor": "left"},
            "template": profile.template,
            "font": {"family": profile.font_family, "size": profile.font_size},
            "paper_bgcolor": profile.background,
            "plot_bgcolor": profile.plot_background,
            "hovermode": profile.hovermode,
            "showlegend": profile.show_legend,
            "legend": legend,
            "height": profile.default_height,
            "width": profile.default_width,
            "margin": {"l": 75, "r": 55, "t": 90, "b": 70},
            "xaxis": {**axis_common, "title": x_title, "rangeslider": {"visible": profile.range_slider}},
            "yaxis": {**axis_common, "title": y_title},
            "dragmode": "zoom",
            "newshape": {"line": {"width": 2}},
        }

    def time_series(
        self,
        series: Sequence[SeriesSpec],
        *,
        title: str = "Time series",
        x_title: str = "Year",
        y_title: str = "Value",
        normalize: bool = False,
        log_y: bool = False,
        reference_lines: Mapping[str, float] | None = None,
    ):
        go, _make_subplots, _pio = _plotly()
        figure = go.Figure()
        buttons_visibility: list[list[bool]] = []
        trace_count = 0
        for index, spec in enumerate(series):
            x_values, y_values, indexes = lttb_downsample(spec.x, spec.y, self.profile.downsample_limit)
            y_array = np.asarray(y_values, dtype=float)
            if normalize:
                finite = y_array[np.isfinite(y_array)]
                scale = float(finite[0]) if len(finite) and abs(float(finite[0])) > 1e-12 else float(np.nanmean(finite)) if len(finite) else 1.0
                y_array = y_array / max(abs(scale), 1e-12)
            color = self.profile.palette[index % len(self.profile.palette)]

            if spec.lower is not None and spec.upper is not None:
                lower = np.asarray(spec.lower, dtype=float)[indexes]
                upper = np.asarray(spec.upper, dtype=float)[indexes]
                if normalize:
                    lower = lower / max(abs(scale), 1e-12)
                    upper = upper / max(abs(scale), 1e-12)
                figure.add_trace(
                    go.Scattergl(
                        x=x_values,
                        y=upper,
                        mode="lines",
                        line={"width": 0},
                        hoverinfo="skip",
                        showlegend=False,
                        name=f"{spec.name} upper",
                        visible=spec.visible,
                    )
                )
                figure.add_trace(
                    go.Scattergl(
                        x=x_values,
                        y=lower,
                        mode="lines",
                        line={"width": 0},
                        fill="tonexty",
                        fillcolor=_rgba(color, 0.16),
                        hoverinfo="skip",
                        showlegend=False,
                        name=f"{spec.name} uncertainty",
                        visible=spec.visible,
                    )
                )
                trace_count += 2

            figure.add_trace(
                go.Scattergl(
                    x=x_values,
                    y=y_array,
                    mode=spec.mode,
                    name=spec.name,
                    line={"width": self.profile.line_width, "color": color},
                    marker={"size": self.profile.marker_size, "color": color},
                    yaxis=spec.yaxis,
                    visible=spec.visible,
                    hovertemplate=spec.hover_template or "%{x}<br>%{y:.5g}<extra>%{fullData.name}</extra>",
                )
            )
            trace_count += 1

        layout = self._base_layout(title, x_title, "Relative to first value" if normalize else y_title)
        layout["yaxis"]["type"] = "log" if log_y else "linear"
        if any(spec.yaxis == "y2" for spec in series):
            layout["yaxis2"] = {
                "title": "Secondary axis",
                "overlaying": "y",
                "side": "right",
                "showgrid": False,
                "showline": True,
                "mirror": True,
                "zeroline": False,
                "showspikes": self.profile.show_spikes,
                "spikemode": "across",
                "spikesnap": "cursor",
            }
            layout["margin"]["r"] = 90
        if reference_lines:
            shapes = []
            annotations = []
            for label, value in reference_lines.items():
                shapes.append({
                    "type": "line", "xref": "paper", "x0": 0, "x1": 1,
                    "yref": "y", "y0": float(value), "y1": float(value),
                    "line": {"dash": "dash", "width": 1.5},
                })
                annotations.append({
                    "xref": "paper", "x": 1, "yref": "y", "y": float(value),
                    "text": str(label), "showarrow": False, "xanchor": "left",
                })
            layout["shapes"] = shapes
            layout["annotations"] = annotations

        # User-facing transform buttons remain available even when the figure is generated from a fixed source.
        layout["updatemenus"] = [
            {
                "type": "buttons",
                "direction": "right",
                "x": 0,
                "y": 1.15,
                "buttons": [
                    {"label": "Linear Y", "method": "relayout", "args": [{"yaxis.type": "linear"}]},
                    {"label": "Log Y", "method": "relayout", "args": [{"yaxis.type": "log"}]},
                    {"label": "Show slider", "method": "relayout", "args": [{"xaxis.rangeslider.visible": True}]},
                    {"label": "Hide slider", "method": "relayout", "args": [{"xaxis.rangeslider.visible": False}]},
                ],
            }
        ]
        figure.update_layout(**layout)
        return figure

    def residual_heatmap(
        self,
        matrix: Sequence[Sequence[float]],
        *,
        x_labels: Sequence[Any] | None = None,
        y_labels: Sequence[Any] | None = None,
        title: str = "Residual heatmap",
        z_title: str = "Standardised residual",
    ):
        go, _make_subplots, _pio = _plotly()
        z = np.asarray(matrix, dtype=float)
        if z.ndim != 2:
            raise ValueError("Residual heatmap requires a two-dimensional matrix.")
        finite = z[np.isfinite(z)]
        limit = max(float(np.nanpercentile(np.abs(finite), 95)) if len(finite) else 1.0, 1e-6)
        figure = go.Figure(
            data=go.Heatmap(
                z=z,
                x=list(x_labels) if x_labels is not None else list(range(z.shape[1])),
                y=list(y_labels) if y_labels is not None else list(range(z.shape[0])),
                zmid=0,
                zmin=-limit,
                zmax=limit,
                colorscale="RdBu",
                reversescale=True,
                colorbar={"title": z_title},
                hovertemplate="X=%{x}<br>Y=%{y}<br>Residual=%{z:.3f}<extra></extra>",
            )
        )
        layout = self._base_layout(title, "Observation / year", "Fleet / age / source")
        layout["xaxis"]["rangeslider"] = {"visible": False}
        figure.update_layout(**layout)
        return figure

    def optimization_surface(
        self,
        rows: Sequence[Mapping[str, Any]],
        *,
        x_key: str,
        y_key: str,
        value_key: str,
        maximize: bool = False,
        title: str = "Parameter optimization surface",
    ):
        """MetaTrader-style parameter grid with an explicit best combination."""

        go, _make_subplots, _pio = _plotly()
        valid: list[tuple[float, float, float]] = []
        for row in rows:
            x_value = _as_float(row.get(x_key))
            y_value = _as_float(row.get(y_key))
            score = _as_float(row.get(value_key))
            if x_value is not None and y_value is not None and score is not None:
                valid.append((x_value, y_value, score))
        if not valid:
            raise ValueError("The optimization grid needs numeric X parameter, Y parameter and objective/fitness values.")
        x_values = sorted({row[0] for row in valid})
        y_values = sorted({row[1] for row in valid})
        x_lookup = {value: index for index, value in enumerate(x_values)}
        y_lookup = {value: index for index, value in enumerate(y_values)}
        cells: dict[tuple[int, int], list[float]] = {}
        for x_value, y_value, score in valid:
            cells.setdefault((y_lookup[y_value], x_lookup[x_value]), []).append(score)
        matrix = np.full((len(y_values), len(x_values)), np.nan, dtype=float)
        for (row_index, column_index), values in cells.items():
            matrix[row_index, column_index] = float(np.mean(values))
        best = max(valid, key=lambda row: row[2]) if maximize else min(valid, key=lambda row: row[2])
        figure = go.Figure()
        figure.add_trace(
            go.Heatmap(
                x=x_values,
                y=y_values,
                z=matrix,
                colorscale="Viridis" if maximize else "Viridis_r",
                colorbar={"title": value_key.replace("_", " ").title()},
                hovertemplate=(
                    f"{x_key}=%{{x:.6g}}<br>{y_key}=%{{y:.6g}}<br>"
                    f"{value_key}=%{{z:.6g}}<extra></extra>"
                ),
            )
        )
        figure.add_trace(
            go.Scatter(
                x=[best[0]],
                y=[best[1]],
                mode="markers+text",
                text=[f"Best: {best[2]:.6g}"],
                textposition="top center",
                name="Best parameter set",
                marker={"symbol": "star", "size": 18, "color": self.profile.palette[1], "line": {"width": 2, "color": self.profile.palette[-1]}},
                hovertemplate=f"Best<br>{x_key}=%{{x:.6g}}<br>{y_key}=%{{y:.6g}}<br>{value_key}={best[2]:.6g}<extra></extra>",
            )
        )
        layout = self._base_layout(title, x_key.replace("_", " ").title(), y_key.replace("_", " ").title())
        layout["xaxis"]["rangeslider"] = {"visible": False}
        layout["annotations"] = [
            {
                "xref": "paper",
                "yref": "paper",
                "x": 0,
                "y": 1.08,
                "showarrow": False,
                "xanchor": "left",
                "text": f"Goal: {'maximize' if maximize else 'minimize'} {value_key.replace('_', ' ')}",
            }
        ]
        figure.update_layout(**layout)
        return figure

    def jitter_distribution(
        self,
        runs: Sequence[Mapping[str, Any]],
        *,
        value_key: str = "objective",
        group_key: str = "optimizer",
        title: str = "Jitter and multi-start distribution",
    ):
        go, _make_subplots, _pio = _plotly()
        groups: dict[str, list[float]] = {}
        for row in runs:
            value = _as_float(row.get(value_key))
            if value is None:
                continue
            groups.setdefault(str(row.get(group_key, "All runs")), []).append(value)
        figure = go.Figure()
        for index, (name, values) in enumerate(sorted(groups.items())):
            color = self.profile.palette[index % len(self.profile.palette)]
            figure.add_trace(
                go.Violin(
                    y=values,
                    name=name,
                    box_visible=True,
                    meanline_visible=True,
                    points="all" if len(values) <= 250 else "outliers",
                    jitter=0.3,
                    marker={"size": max(3.0, self.profile.marker_size - 2), "color": color},
                    line={"color": color, "width": self.profile.line_width},
                )
            )
        layout = self._base_layout(title, "Optimizer / configuration", value_key.replace("_", " ").title())
        layout["xaxis"]["rangeslider"] = {"visible": False}
        figure.update_layout(**layout)
        return figure

    def optimizer_agreement(
        self,
        rows: Sequence[Mapping[str, Any]],
        *,
        x_key: str = "objective",
        y_key: str = "terminal_depletion",
        label_key: str = "optimizer",
        size_key: str | None = None,
        title: str = "Multi-optimizer agreement",
    ):
        go, _make_subplots, _pio = _plotly()
        groups: dict[str, list[Mapping[str, Any]]] = {}
        for row in rows:
            groups.setdefault(str(row.get(label_key, "run")), []).append(row)
        figure = go.Figure()
        for index, (name, group) in enumerate(sorted(groups.items())):
            x = [_as_float(row.get(x_key)) for row in group]
            y = [_as_float(row.get(y_key)) for row in group]
            valid = [(a, b, row) for a, b, row in zip(x, y, group) if a is not None and b is not None]
            if not valid:
                continue
            color = self.profile.palette[index % len(self.profile.palette)]
            sizes = []
            for _a, _b, row in valid:
                raw = _as_float(row.get(size_key)) if size_key else None
                sizes.append(max(self.profile.marker_size, min(30.0, raw or self.profile.marker_size)))
            figure.add_trace(
                go.Scattergl(
                    x=[value[0] for value in valid],
                    y=[value[1] for value in valid],
                    mode="markers+text" if len(valid) <= 12 else "markers",
                    text=[str(value[2].get("run", "")) for value in valid],
                    textposition="top center",
                    name=name,
                    marker={"size": sizes, "color": color, "opacity": 0.78, "line": {"width": 1}},
                    customdata=[json.dumps(value[2], default=str) for value in valid],
                    hovertemplate=f"{x_key}=%{{x:.6g}}<br>{y_key}=%{{y:.6g}}<extra>{html.escape(name)}</extra>",
                )
            )
        layout = self._base_layout(title, x_key.replace("_", " ").title(), y_key.replace("_", " ").title())
        layout["xaxis"]["rangeslider"] = {"visible": False}
        figure.update_layout(**layout)
        return figure

